package com.Rice.entity;
import java.io.Serializable;
import java.util.Date;
public class Consumption implements Serializable{
	private int cId;
	private Date cTime;
	private String cOrderNumber;
	private Double cMoney;
	private Member mId;
	public int getcId() {
		return cId;
	}
	public Date getcTime() {
		return cTime;
	}
	public String getcOrderNumber() {
		return cOrderNumber;
	}
	public void setcId(int cId) {
		this.cId = cId;
	}
	public void setcTime(Date cTime) {
		this.cTime = cTime;
	}
	public void setcOrderNumber(String cOrderNumber) {
		this.cOrderNumber = cOrderNumber;
	}
	public Double getcMoney() {
		return cMoney;
	}
	public void setcMoney(Double cMoney) {
		this.cMoney = cMoney;
	}
	public Member getmId() {
		return mId;
	}
	public void setmId(Member mId) {
		this.mId = mId;
	}
	@Override
	public String toString() {
		return "Consumption [cId=" + cId + ", cTime=" + cTime + ", cOrderNumber=" + cOrderNumber + ", cMoney=" + cMoney
				+ ", mId=" + mId + "]";
	}
}
