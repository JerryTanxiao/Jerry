package com.Rice.entity;

import java.io.Serializable;
import java.util.List;

public class Member implements Serializable {
	private static final long serialVersionUID = 1L;
	private int mId;
	private String mName;//会员名
	private String mVipLevel;//会员等级
	private String mPhone;//会员
	private List<Consumption> mConsumption;//消费记录
	private List<Recharge> mRecharge;//充值记录
	private Double mPrice;//原价
	private Double mDiscount;//折后
	private String mUsername;//外键名称
	public int getmId() {
		return mId;
	}
	public String getmName() {
		return mName;
	}
	public String getmUsername() {
		return mUsername;
	}
	public String getmVipLevel() {
		return mVipLevel;
	}
	public String getmPhone() {
		return mPhone;
	}
	public List<Consumption> getmConsumption() {
		return mConsumption;
	}
	public List<Recharge> getmRecharge() {
		return mRecharge;
	}
	public Double getmPrice() {
		return mPrice;
	}
	public Double getmDiscount() {
		return mDiscount;
	}
	public void setmId(int mId) {
		this.mId = mId;
	}
	public void setmName(String mName) {
		this.mName = mName;
	}
	public void setmUsername(String mUsername) {
		this.mUsername = mUsername;
	}
	public void setmVipLevel(String mVipLevel) {
		this.mVipLevel = mVipLevel;
	}
	public void setmPhone(String mPhone) {
		this.mPhone = mPhone;
	}
	public void setmConsumption(List<Consumption> mConsumption) {
		this.mConsumption = mConsumption;
	}
	public void setmRecharge(List<Recharge> mRecharge) {
		this.mRecharge = mRecharge;
	}
	public void setmPrice(Double mPrice) {
		this.mPrice = mPrice;
	}
	public void setmDiscount(Double mDiscount) {
		this.mDiscount = mDiscount;
	}
	@Override
	public String toString() {
		return "Member [mId=" + mId + ", mName=" + mName + ", mUsername=" + mUsername + ", mVipLevel=" + mVipLevel
				+ ", mPhone=" + mPhone + ", mConsumption=" + mConsumption + ", mRecharge=" + mRecharge + ", mPrice="
				+ mPrice + ", mDiscount=" + mDiscount + "]";
	}
	
}
